defmodule WeirdoLottery.Users do
  @moduledoc """
  The Users context.
  """

  import Ecto.Query, warn: false
  alias WeirdoLottery.Repo

  alias WeirdoLottery.Users.User

  @doc """
  Returns the list of users.

  ## Examples

      iex> list_users()
      [%User{}, ...]

  """
  def list_users do
    Repo.all(User)
  end

  @doc """
  Gets a single user.

  Raises `Ecto.NoResultsError` if the User does not exist.

  ## Examples

      iex> get_user!(123)
      %User{}

      iex> get_user!(456)
      ** (Ecto.NoResultsError)

  """
  def get_user!(id), do: Repo.get!(User, id)

  @doc """
  Creates a user.

  ## Examples

      iex> create_user(%{field: value})
      {:ok, %User{}}

      iex> create_user(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_user(attrs \\ %{}) do
    %User{}
    |> User.changeset(attrs)
    |> Repo.insert()
  end

  @doc """
  Updates a user.

  ## Examples

      iex> update_user(user, %{field: new_value})
      {:ok, %User{}}

      iex> update_user(user, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_user(%User{} = user, attrs) do
    user
    |> User.changeset(attrs)
    |> Repo.update()
  end

  @doc """
  Deletes a user.

  ## Examples

      iex> delete_user(user)
      {:ok, %User{}}

      iex> delete_user(user)
      {:error, %Ecto.Changeset{}}

  """
  def delete_user(%User{} = user) do
    Repo.delete(user)
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking user changes.

  ## Examples

      iex> change_user(user)
      %Ecto.Changeset{data: %User{}}

  """
  def change_user(%User{} = user, attrs \\ %{}) do
    User.changeset(user, attrs)
  end

  @spec draw_winners(integer()) :: list(User.t())
  @doc """
  Returns a list with up to 2 winning `User{}` elements.

  A user is a potential winner if they have more `points` than
  the given `points_threshold`.

  The winners are drawn in a random way.

  ## Examples

      iex> draw_winners(23)
      [%User{}, %User{}]

  """
  def draw_winners(points_threshold \\ 0) do
    query =
      from u in User,
        where: u.points > ^points_threshold,
        order_by: fragment("RANDOM()"),
        select: [:id, :points],
        limit: 2

    Repo.all(query)
  end
end
